//! Compressed Proof Types
//!
//! Types for batch-compressed hexary proofs using STWO.
//! Multiple hexary proofs are aggregated into a single STARK proof
//! for efficient on-chain verification.

use crate::core::value::Value;
use crate::trie::proof::HexaryProof;
use crate::zk::proof::{SerializationError, SolanaSerialize, StarkProof};
use crate::zk::bundled::MERKLE_BATCH_HASH;

/// Average size of a hexary proof in bytes (empirical measurement)
pub const AVG_HEXARY_PROOF_SIZE: usize = 68;

/// Maximum number of proofs in a single compressed batch
pub const MAX_BATCH_SIZE: u64 = 10_000;

/// Maximum compressed proof size (including STARK proof)
pub const MAX_COMPRESSED_SIZE: usize = 500 * 1024; // 500 KB

/// A compressed STARK proof that represents multiple hexary proofs
///
/// Instead of verifying each hexary proof individually, we batch them
/// into a single STARK proof generated by the merkle_batch.cairo program.
#[derive(Debug, Clone, PartialEq, Eq)]
pub struct CompressedProof {
    /// Hash of the Cairo program (merkle_batch.cairo)
    pub program_hash: [u8; 32],

    /// Number of rows/proofs in this batch
    pub row_count: u64,

    /// Expected state root after applying all operations
    pub root: [u8; 32],

    /// The STARK proof from STWO proving all hexary proofs
    pub stark_proof: StarkProof,
}

impl CompressedProof {
    /// Create a new compressed proof
    pub fn new(
        row_count: u64,
        root: [u8; 32],
        stark_proof: StarkProof,
    ) -> Self {
        Self {
            program_hash: MERKLE_BATCH_HASH,
            row_count,
            root,
            stark_proof,
        }
    }

    /// Create a compressed proof with a custom program hash
    pub fn with_program_hash(
        program_hash: [u8; 32],
        row_count: u64,
        root: [u8; 32],
        stark_proof: StarkProof,
    ) -> Self {
        Self {
            program_hash,
            row_count,
            root,
            stark_proof,
        }
    }

    /// Calculate the compression ratio
    ///
    /// Returns how much smaller the compressed proof is compared to
    /// the original individual hexary proofs.
    ///
    /// # Formula
    ///
    /// ```text
    /// compression_ratio = original_size / compressed_size
    ///
    /// where:
    ///   original_size = row_count * AVG_HEXARY_PROOF_SIZE
    ///   compressed_size = stark_proof.proof.len() + header_size
    ///   header_size = 72 bytes (program_hash + row_count + root)
    /// ```
    pub fn compression_ratio(&self) -> f64 {
        let original_size = self.row_count as usize * AVG_HEXARY_PROOF_SIZE;
        let compressed_size = self.stark_proof.proof.len() + 72;

        if compressed_size == 0 {
            return 1.0;
        }

        original_size as f64 / compressed_size as f64
    }

    /// Calculate the original (uncompressed) size in bytes
    pub fn original_size(&self) -> usize {
        self.row_count as usize * AVG_HEXARY_PROOF_SIZE
    }

    /// Calculate the compressed size in bytes
    pub fn compressed_size(&self) -> usize {
        self.stark_proof.proof.len() + 72
    }

    /// Validate the compressed proof structure
    pub fn validate(&self) -> Result<(), CompressedProofError> {
        // Check row count
        if self.row_count == 0 {
            return Err(CompressedProofError::EmptyBatch);
        }
        if self.row_count > MAX_BATCH_SIZE {
            return Err(CompressedProofError::BatchTooLarge(self.row_count));
        }

        // Check STARK proof validity
        self.stark_proof.validate()
            .map_err(|e| CompressedProofError::InvalidStarkProof(format!("{:?}", e)))?;

        // Check compressed size limit
        if self.compressed_size() > MAX_COMPRESSED_SIZE {
            return Err(CompressedProofError::ProofTooLarge(self.compressed_size()));
        }

        // Check program hash is valid (not all zeros)
        if self.program_hash == [0u8; 32] {
            return Err(CompressedProofError::InvalidProgramHash);
        }

        Ok(())
    }

    /// Verify the compressed proof
    ///
    /// This method verifies that:
    /// 1. The proof structure is valid
    /// 2. The program hash is registered in the global registry
    /// 3. The STARK proof is valid
    ///
    /// Returns `Ok(true)` if verification succeeds, or an error if it fails.
    #[cfg(feature = "zk")]
    /// Verify the compressed proof using STWO plugin
    ///
    /// Requires the STWO plugin to be available.
    /// Set `STOOLAP_STWO_PLUGIN` environment variable or ensure
    /// `../stwo-plugin/target/release/libstwo_plugin.so` exists.
    ///
    /// # Errors
    ///
    /// Returns `CompressedProofError::PluginNotFound` if plugin is not available.
    pub fn verify(&self) -> Result<bool, CompressedProofError> {
        // 1. Validate proof structure first
        self.validate()?;

        // 2. Load and use plugin (no fallback - intentional)
        let plugin = crate::zk::plugin::load_plugin()
            .map_err(CompressedProofError::PluginNotFound)?;

        // 3. Verify proof using plugin
        plugin.verify(&self.stark_proof.proof)
            .map_err(CompressedProofError::PluginError)
    }

    /// Quick verification check (without full STARK verification)
    ///
    /// This is a faster check that validates the proof structure
    /// and checks the program hash without doing full STARK verification.
    /// Use this for quick rejection of invalid proofs before doing
    /// expensive STARK verification.
    pub fn quick_verify(&self) -> Result<bool, CompressedProofError> {
        // Validate structure
        self.validate()?;

        // Check program is registered (without full verification)
        #[cfg(feature = "zk")]
        {
            let registry = crate::zk::CairoProgramRegistry::get_global();
            if registry.get(&self.program_hash).is_none() {
                return Err(CompressedProofError::UnregisteredProgram);
            }
        }

        Ok(true)
    }

    /// Get the size savings as a percentage
    pub fn space_savings_percentage(&self) -> f64 {
        let ratio = self.compression_ratio();
        if ratio <= 1.0 {
            return 0.0;
        }
        (1.0 - (1.0 / ratio)) * 100.0
    }
}

impl SolanaSerialize for CompressedProof {
    fn serialize(&self) -> Vec<u8> {
        let mut buffer = Vec::with_capacity(80);

        // program_hash (32 bytes)
        buffer.extend_from_slice(&self.program_hash);

        // row_count (8 bytes, little-endian)
        buffer.extend_from_slice(&self.row_count.to_le_bytes());

        // root (32 bytes)
        buffer.extend_from_slice(&self.root);

        // stark_proof (variable length)
        buffer.extend(self.stark_proof.serialize());

        buffer
    }

    fn deserialize(data: &[u8]) -> Result<Self, SerializationError> {
        if data.len() < 72 {
            return Err(SerializationError::DataTooShort);
        }

        let mut offset = 0;

        // program_hash (32 bytes)
        let mut program_hash = [0u8; 32];
        program_hash.copy_from_slice(&data[offset..offset + 32]);
        offset += 32;

        // row_count (8 bytes)
        let row_count = u64::from_le_bytes(data[offset..offset + 8].try_into().unwrap());
        offset += 8;

        // root (32 bytes)
        let mut root = [0u8; 32];
        root.copy_from_slice(&data[offset..offset + 32]);
        offset += 32;

        // stark_proof (remaining data)
        let stark_proof = StarkProof::deserialize(&data[offset..])?;

        Ok(Self {
            program_hash,
            row_count,
            root,
            stark_proof,
        })
    }

    fn serialized_size(&self) -> usize {
        72 + self.stark_proof.serialized_size()
    }
}

/// Input data for batch verification
///
/// Contains all the information needed to verify a batch of hexary proofs
/// by generating a compressed STARK proof.
#[derive(Debug, Clone, PartialEq, Eq)]
pub struct BatchVerifyInput {
    /// Row IDs being proven
    pub row_ids: Vec<i64>,

    /// Values for each row
    pub values: Vec<Value>,

    /// Individual hexary proofs
    pub proofs: Vec<HexaryProof>,

    /// Expected root after verification
    pub expected_root: [u8; 32],
}

impl BatchVerifyInput {
    /// Create a new batch verification input
    pub fn new(
        row_ids: Vec<i64>,
        values: Vec<Value>,
        proofs: Vec<HexaryProof>,
        expected_root: [u8; 32],
    ) -> Self {
        Self {
            row_ids,
            values,
            proofs,
            expected_root,
        }
    }

    /// Get the number of proofs in the batch
    pub fn len(&self) -> usize {
        self.proofs.len()
    }

    /// Check if the batch is empty
    pub fn is_empty(&self) -> bool {
        self.proofs.is_empty()
    }

    /// Validate the batch input
    pub fn validate(&self) -> Result<(), BatchVerifyError> {
        // Check all vectors have the same length
        if self.row_ids.len() != self.proofs.len() {
            return Err(BatchVerifyError::LengthMismatch(
                "row_ids",
                self.row_ids.len(),
                self.proofs.len(),
            ));
        }
        if self.values.len() != self.proofs.len() {
            return Err(BatchVerifyError::LengthMismatch(
                "values",
                self.values.len(),
                self.proofs.len(),
            ));
        }

        // Check batch not empty
        if self.proofs.is_empty() {
            return Err(BatchVerifyError::EmptyBatch);
        }

        // Check batch size limit
        if self.proofs.len() as u64 > MAX_BATCH_SIZE {
            return Err(BatchVerifyError::BatchTooLarge(self.proofs.len()));
        }

        Ok(())
    }

    /// Calculate the original size of all hexary proofs
    pub fn original_size(&self) -> usize {
        self.proofs.len() * AVG_HEXARY_PROOF_SIZE
    }
}

/// Errors related to compressed proofs
#[derive(Debug, Clone, PartialEq, Eq)]
pub enum CompressedProofError {
    /// Batch has no proofs
    EmptyBatch,
    /// Batch exceeds maximum size
    BatchTooLarge(u64),
    /// STARK proof is invalid
    InvalidStarkProof(String),
    /// Compressed proof exceeds size limit
    ProofTooLarge(usize),
    /// Program hash is invalid (all zeros)
    InvalidProgramHash,
    /// Program is not registered in the global registry
    UnregisteredProgram,
    /// Plugin was not found (STWO not available)
    PluginNotFound,
    /// Plugin error occurred
    PluginError(crate::zk::plugin::PluginError),
    /// Serialization error
    SerializationError(SerializationError),
}

impl std::fmt::Display for CompressedProofError {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        match self {
            CompressedProofError::EmptyBatch => write!(f, "Batch contains no proofs"),
            CompressedProofError::BatchTooLarge(size) => {
                write!(f, "Batch size {} exceeds maximum {}", size, MAX_BATCH_SIZE)
            }
            CompressedProofError::InvalidStarkProof(msg) => {
                write!(f, "Invalid STARK proof: {}", msg)
            }
            CompressedProofError::ProofTooLarge(size) => {
                write!(f, "Proof size {} exceeds maximum {}", size, MAX_COMPRESSED_SIZE)
            }
            CompressedProofError::InvalidProgramHash => {
                write!(f, "Program hash is invalid (all zeros)")
            }
            CompressedProofError::UnregisteredProgram => {
                write!(f, "Program is not registered in the global registry")
            }
            CompressedProofError::PluginNotFound => {
                write!(f, "STWO plugin not found. Set STOOLAP_STWO_PLUGIN environment variable or build stwo-plugin crate.")
            }
            CompressedProofError::PluginError(e) => {
                write!(f, "STWO plugin error: {}", e)
            }
            CompressedProofError::SerializationError(e) => {
                write!(f, "Serialization error: {:?}", e)
            }
        }
    }
}

impl std::error::Error for CompressedProofError {}

impl From<SerializationError> for CompressedProofError {
    fn from(err: SerializationError) -> Self {
        CompressedProofError::SerializationError(err)
    }
}

impl From<crate::zk::plugin::PluginError> for CompressedProofError {
    fn from(err: crate::zk::plugin::PluginError) -> Self {
        match err {
            crate::zk::plugin::PluginError::NotFound => CompressedProofError::PluginNotFound,
            e => CompressedProofError::PluginError(e),
        }
    }
}

/// Errors related to batch verification inputs
#[derive(Debug, Clone, PartialEq, Eq)]
pub enum BatchVerifyError {
    /// Vector length mismatch
    LengthMismatch(&'static str, usize, usize),
    /// Batch is empty
    EmptyBatch,
    /// Batch exceeds maximum size
    BatchTooLarge(usize),
}

impl std::fmt::Display for BatchVerifyError {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        match self {
            BatchVerifyError::LengthMismatch(name, expected, actual) => {
                write!(
                    f,
                    "Length mismatch for '{}': expected {}, got {}",
                    name, expected, actual
                )
            }
            BatchVerifyError::EmptyBatch => write!(f, "Batch contains no proofs"),
            BatchVerifyError::BatchTooLarge(size) => {
                write!(f, "Batch size {} exceeds maximum {}", size, MAX_BATCH_SIZE)
            }
        }
    }
}

impl std::error::Error for BatchVerifyError {}

